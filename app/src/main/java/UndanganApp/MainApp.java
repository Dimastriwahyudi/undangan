    private void btn_simpanActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btn_simpanActionPerformed
        int i = list_undangan.getSelectedIndex();
//        System.out.println(i);
        if (i == -1) {
            //peringatan
            JOptionPane.showMessageDialog(this, "Anda belum memilih list undangan");
        } else {
            //simpan data
            String nama = txt_namaTamu.getText();
            String alamat = txtAlamat.getText();
            String temKerja = txtTempatKerja.getText();
            String id = list_undangan.getSelectedItem().toString();
            String[] id_data = id.split("\\|");
            String id_u = id_data[0];

            try {
                Connection k = Koneksi.Go();
                Statement s = k.createStatement();
                String q = "INSERT INTO undangan_detail "
                        + "(id_u,nama_tamu,alamat,tempat_kerja) "
                        + "VALUES ('" + id_u + "','" + nama + "','" + alamat + "','" + temKerja + "')";
                s.executeUpdate(q);

                refreshtable();
            } catch (SQLException e) {
                System.err.println("Error: " + e.getMessage());
            }

        }
    }//GEN-LAST:event_btn_simpanActionPerformed

