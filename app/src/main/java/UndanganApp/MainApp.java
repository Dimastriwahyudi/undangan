    public static void refreshList() {
        try {
            String q = "SELECT * FROM undangan";
            Connection k = Koneksi.Go();
            Statement s = k.createStatement();
            ResultSet r = s.executeQuery(q);
            list_undangan.removeAllItems();
            filter_list.removeAllItems();
            while (r.next()) {
                int id = r.getInt("id");
                String nama = r.getString("nama_undangan");
                list_undangan.addItem(id + "|" + nama);
                filter_list.addItem(id + "|" + nama);
            }

        } catch (Exception e) {
        }
    }

    public static void refreshtable() {
        try {
            DefaultTableModel m = (DefaultTableModel) table_data.getModel();

            String q = "SELECT * FROM undangan_detail";
            Connection k = Koneksi.Go();
            Statement s = k.createStatement();
            ResultSet r = s.executeQuery(q);
            m.getDataVector().removeAllElements();
            //int no = 1;
            while (r.next()) {
                int id = r.getInt("id");
                int id_u = r.getInt("id_u");
                String nama = r.getString("nama_tamu");
                String alamat = r.getString("alamat");
                String tempat_kerja = r.getString("tempat_kerja");
                String status_kehadiran = r.getString("status_kehadiran");
                Object[] data = {id, id_u, nama, alamat, tempat_kerja, status_kehadiran};
                m.addRow(data);
                //no++;                
            }

        } catch (Exception e) {
        }

    }

